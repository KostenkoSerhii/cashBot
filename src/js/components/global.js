export function isResp991() {
	let windowWidth = $(document).width();

	if (windowWidth < 992) return true;
	return false;
}
export function isResp1170() {
	let windowWidth = $(document).width();

	if (windowWidth < 1170) return true;
	return false;
}

export function isResp767() {
	let windowWidth = $(document).width();

	if (windowWidth < 768) return true;
	return false;
}
